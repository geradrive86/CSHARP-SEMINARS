// Задание 2: Поиск суммы элементов в каждом столбце
// Описание: Задайте двумерный массив целых чисел. Напишите программу, которая
// находит сумму элементов в каждом столбце массива и выводит её.

int[,] array = {
            { 1, 3, 5, 7 },
            { 0, 6, 2, 8 },
            { 9, 4, 1, 5 }
        };
for (int j = 0; j < array.GetLength(1); j++) // Перебор столбцов
{
    int sum = 0; // Переменная для хранения суммы
    for (int i = 0; i < array.GetLength(0); i++) // Перебор строк в столбце
    {
        sum += array[i, j]; // Добавление элемента к сумме
    }
    Console.WriteLine($"Сумма в столбце {j}: {sum}");
}