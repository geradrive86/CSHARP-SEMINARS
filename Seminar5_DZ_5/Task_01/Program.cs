// Задание 1: Поиск максимального элемента в каждой строке
// Описание: Задайте двумерный массив целых чисел. Напишите программу, которая
// находит максимальный элемент в каждой строке массива и выводит его.

int[,] array =
    {
    { 1, 3, 5, 7 },
    { 0, 6, 2, 8 },
    { 9, 4, 1, 5 }
    };
for (int i = 0; i < array.GetLength(0); i++) // Перебор строк
{
    int max = array[i, 0]; // Начальное значение для поиска максимума
    for (int j = 1; j < array.GetLength(1); j++) // Перебор элементов в строке
    {
        if (array[i, j] > max)
        {
            max = array[i, j]; // Обновление максимального значения
        }
    }
    Console.WriteLine($"Максимум в строке {i}: {max}");
}