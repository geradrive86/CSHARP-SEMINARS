//Задача 1: Напишите программу, которая бесконечно запрашивает целые числа с консоли. Программа завершается при вводе символа ‘q’ или при вводе числа, сумма цифр которого чётная.

while (true) // Бесконечный цикл
{
    Console.Write("Введите целое число (или 'q' для выхода): ");
    string input = Console.ReadLine(); // Считываем ввод пользователя
    if (input.ToLower() == "q") // Проверяем, ввел ли пользователь 'q'
    {
        break; // Выходим из цикла
    }
    if (int.TryParse(input, out int number)) // Пробуем преобразовать ввод в число
    {
        int sumOfDigits = 0; // Переменная для хранения суммы цифр
        int temp = number; // Временная переменная для вычисления суммы цифр
        while (temp != 0) // Цикл для вычисления суммы цифр
        {
            sumOfDigits += Math.Abs(temp % 10); // Добавляем последнюю цифру к сумме
            temp /= 10; // Убираем последнюю цифру
        }
        if (sumOfDigits % 2 == 0) // Проверяем, является ли сумма цифр четной
        {
            Console.WriteLine("Сумма цифр числа четная. Завершаем программу.");
            break; // Выходим из цикла
        }
    }
    else
    {
        Console.WriteLine("Пожалуйста, введите корректное целое число.");
    }
}


