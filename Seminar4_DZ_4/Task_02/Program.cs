//Задача 2: //Задайте массив заполненный случайными трёхзначными числами. Напишите программу, которая покажет количество чётных чисел в массиве.

int[] array = new int[10]; // Создаем массив на 10 элементов
Random random = new Random(); // Создаем объект для генерации случайных чисел
for (int i = 0; i < array.Length; i++)
{
    array[i] = random.Next(100, 1000); // Заполняем массив случайными трехзначными числами
}
int evenCount = 0; // Счетчик четных чисел
foreach (int number in array)
{
    if (number % 2 == 0) // Проверка числа на четность
        evenCount++;
}
Console.WriteLine($"Количество четных чисел в массиве: {evenCount}");